int	ft_sqrt(int nb)
{
	int	i;
	int	rule;

	i = 1;
	rule = 0;
	if (nb < 0)
		return (0);
	else if (nb == 0 || nb == 1)
		return (nb);
	if (nb > 0)
	{
		while (i <= nb / 2)
		{
			if (i * i == nb)
			{
				rule = i;
				break ;
			}
			i++;
		}
	}
	return (rule);
}
